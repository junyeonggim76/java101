package day08;

public class Calc {
    //어떠한 두 x,y가 들어왔을때 더해주는 함수랑 빼주는거 곱하는거 나누는거
    int plus(int x, int y){
        return x + y;
    }
    int minus(int x, int y){
        return x - y;
    }
    int multiply(int x, int y){
        return x * y;
    }
    int divide(int x, int y){
        return x / y;
    }

}
